<?php

namespace Prj\GH;

/**
 * 国槐的copartner列表
 *
 * @author simon.wang
 */
class GHCopartner extends \Sooh2\DB\KVObj
{

    public function onInit()
    {
        $this->field_locker=null;
        parent::onInit(); // TODO: Change the autogenerated stub
        $this->_tbName = 'jz_channel_info';//表名的默认模板
    }

    /**
     * 
     * @return \Prj\GH\GHCopartner
     */
    public static function getByCopartnerCode($n4)
    {
        return parent::getCopy(array('channelSource'=>$n4));
    }

    public function getCopartnerName()
    {
        return $this->getField('channelName');
    }

    public function getCopartnerCode()
    {
        return $this->getField('channelSource');
    }

}