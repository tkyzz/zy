namespace Prj\Model\Payment;

use Prj\Model\_ModelBase;

class InvestorAssetTotal extends _ModelBase
{
    protected static $pkeyName = 'userId';

    public function onInit()
    {
        $this->className = "Payment";
        parent::onInit(); // TODO: Change the autogenerated stub
        $this->_tbName = "t_investor_asset_total";
    }

    /**
     * Hand 获取用户的余额
     * @param $userId
     * @return null
     */
    public static function getWallet($userId){
        $model = self::getCopy($userId);
        $model->load(true);
        if(!$model->exists())return null;
        return $model->getField('availableBalance');
    }
}