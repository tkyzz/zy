namespace Prj\Model;
class Protocol extends \Rpt\Manage\_ModelBase
{
    public function onInit()
    {
        $this->className = "ProductList";
        parent::onInit(); // TODO: Change the autogenerated stub
        $this->_tbName = "tb_protocol";
    }

    /**
     * 获取协议列表
     * @param type Void
     * @return string
     */
    public function getVersionList($where = []){
        $data = self::getRecords('id,type,version,updateTime,createTime' , $where);
        return $data;
    }

    /**
     * 获取协议详情
     * @param type Void
     * @return string
     */
    public static function getVersionDetail($where = [],$order){
        if( empty($where) ){
            return false;
        }
        $data = self::getRecord('id,type,version,content,updateTime,createTime' , $where , $order);
//        print_r(\Prj\Model\Product::getRecord("*"));exit();
        return $data;
    }

}