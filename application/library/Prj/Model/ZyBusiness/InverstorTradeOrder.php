namespace Prj\Model\ZyBusiness;
use Prj\Model\_ModelBase;

class InverstorTradeOrder extends _ModelBase
{
    public function onInit()
    {
        $this->className = "ZyBusiness";
        parent::onInit(); // TODO: Change the autogenerated stub
        $this->_tbName = "tpf_investor_trade_order";

    }

    //根据订单查询产品信息
    public static function getProductDetail($orderId){
        if( empty($orderId) )return false;
        $orderInfo = parent::getRecord('*',['orderNo'=>$orderId]);
        return ProductInfo::getRecord('*',['productId'=>$orderInfo['productId']]);
    }
}