namespace Prj\Model\ZyBusiness;

use Prj\Model\_ModelBase;

class PlatformChannel extends _ModelBase {
    public function onInit()
    {
        $this->className = "ZyBusiness";
        parent::onInit(); // TODO: Change the autogenerated stub
        $this->_tbName = "tpf_platform_channel";

    }


    public static function getChannel(){
        list($db,$tb)=self::getCopy('')->dbAndTbName();
        $rs=$db->getPair($tb,'channelId','channelName');
        return $rs;
    }



}